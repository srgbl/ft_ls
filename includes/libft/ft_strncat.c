#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*p;
	size_t	i;

	i = 0;
	p = s1;
	while (*s1)
		s1++;
	while (*s2 && i++ < n)
		*s1++ = *s2++;
	*s1 = '\0';
	return (p);
}
